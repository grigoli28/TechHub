let generateHTML = function({ mainSelector, parentElem, parentCount, parentCont = null, pAttr1Name = null, pAttr1Value = null, pAttr2Name = null, pAttr2Value = null, childElement = null, childCount = 1, childCont = null, chAttr1Name = null, chAttr1Value = null, chAttr2Name = null, chAttr2Value = null }) {
    let mainNode = document.querySelector(mainSelector);
    for (let i = 0; i < parentCount; i++) {
        let parentNode = document.createElement(parentElem);
        if (pAttr1Name && pAttr1Value) // if parent has first attribute
            parentNode.setAttribute(pAttr1Name, pAttr1Value);
        if (pAttr2Name && pAttr2Value) // if parent has second attribute
            parentNode.setAttribute(pAttr2Name, pAttr2Value);
        if (parentCont)
            parentNode.appendChild(document.createTextNode(parentCont));
        if (childElement) {
            for (let i = 0; i < childCount; i++) {
                let childNode = document.createElement(childElement);
                if (chAttr1Name && chAttr1Value) // if child has first attribute
                    childNode.setAttribute(chAttr1Name, chAttr1Value);
                if (chAttr2Name && chAttr2Value) // if child has second attribute
                    childNode.setAttribute(chAttr2Name, chAttr2Value);
                if (childCont)
                    childNode.appendChild(document.createTextNode(childCont));
                parentNode.appendChild(childNode);
            }
        }
        mainNode.appendChild(parentNode);
    }
}

